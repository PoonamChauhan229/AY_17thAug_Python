# for in loop
# iteration >>run again after value increase
# iterate over a sequence
# range >> built in function >> squence of nmumber
# Print 1 to 5
# range >> loops >> start stop 

# range >> start stop step 
# range(1,5,1)

# start stop step 
for i in range(1,6,1):
    print(i)

print("---------------")

# stop point , default start 0 , default step 1
# no start >> start always at 0
# no step 
# iterate over the range 5 times
for i in range(6):
    print(i)

print("---------------")

# start , stop >> default step 1
for i in range(2,6):
    print(i)

print("---------------")
# start 5 , end 10 >> print 5-9 >> step 2 >> increment by 2
for i in range(5,10,2):
    print(i)

print("---------------")
for i in range(5,10,1):
    print(i)
